package D4_if_Quiz;

import java.util.Random;
import java.util.Scanner;
/*
 * # ATM[1단계] : 이체하기
 * 1. 이체할 계좌번호를 입력받는다.
 * 2. 계좌번호가 일치하면,
 * 3. 이체할 금액을 입력받는다.
 * 4. 이체할 금액 <= myMoney	: 이체가능
 * 			myMoney   - 이체할 금액
 * 			yourMoney + 이체할 금액
 *    이체할 금액  > myMoney 	: 이체불가
 */
public class D4_if1_ATM문제 {
	public static void main(String[] args) {
		// your_acc : 스네이크 : db (db에서 많이쓰는 방식)
		// yourAcc : 카멜 : java (wep에서는 이게 법)
		Scanner scan = new Scanner(System.in);
		
		int myAcc = 1234;
		int myMoney = 8700;
		
		int yourAcc = 4321;
		int yourMoney = 12000;
		
		System.out.println("계좌번호를 입력하세요.");
		int 계좌 = scan.nextInt();
		
		if(myAcc == 계좌) {
			System.out.println("이체하실 금액을 입력하세요.");
			int 이체금액 = scan.nextInt();
			if(이체금액 <= myMoney) {
				System.out.println("이체가능");
				System.out.println("이체하실 계좌번호를 입력하세요.");
				int 이체계좌 = scan.nextInt();
				if(yourAcc == 이체계좌) {
					System.out.println("이체성공");
					myMoney = myMoney - 이체금액;
					yourAcc = yourMoney + 이체금액;
					System.out.println("myMoney = " + myMoney + "원");
					System.out.println("yourMoney = " + yourMoney + "원");
				}
				else {
					System.out.println("이체하실 계좌번호를 확인해주세요.");
				}
			}
			else{
				System.out.println("이체불가 금액을 확인해주세요.");
			}
		}
		else {
			System.out.println("계좌번호를 확인해주세요.");
		}		
	}
}
