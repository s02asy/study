package C2_Operator_Quiz;

public class C2_Operator1_문제 {
	public static void main(String[] args) {
		
		//문제1) 아래값이 정답이되도록 a 값을 변경해보자.

	    int a = 3;
	    boolean b = 2 * a - 1 > 4 ;
	    System.out.println(b);
	   

	    //문제2) 아래값이 정답이되도록 a 값을 변경해보자.

	    a = 5;
	    b = 6 - 2 * a < a ;
	    System.out.println(b);
	   

	    //문제3) 아래값이 정답이되도록 a 값을 변경해보자.

	    a = 50;
	    b = 63 * a - 5 > 7 + 2 * a;
	    System.out.println(b);
	}
}
